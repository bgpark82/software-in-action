package com.bgpark

/**
 * const
 * - compile time value
 * - not for local value
 * - uppercase naming
 */
const val CONSTANT = 1

fun main() {
    /**
     * var
     * - mutable
     * - getter/setter in class
     */
    var a = 2
    a = 3

    /**
     * val
     * - immutable (final)
     * - getter
     */
    val b = 2
    // b = 3 (reassign not possible)


}